function myFunction() {
    var mereText = document.getElementById("mere");
    var læsMerePil = document.getElementById("læsmerepil")
  
    if (læsMerePil.style.transform === "rotate(-180deg)"){
      læsMerePil.style.transform = "rotate(0)"; 
      mereText.style.display = "none";
    } else {
        læsMerePil.style.transform = "rotate(-180deg)"; 
        mereText.style.display = "inline";
    }
  }
